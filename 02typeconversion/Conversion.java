
public class Conversion {
    public static void main(String[] args) {

        int a = 45;
        byte b = (byte) a;
        float fi = 45.56f;

        int x = (int) fi;

        System.out.println(b);
        System.out.println(x);

        byte a1 = 10;
        byte b1 = 30;
        int result = a1 * b1;
        System.out.print(result);

    }

}
